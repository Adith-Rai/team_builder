# ---- helpers added for tooling / evaluation ----
# Provides: TEAMS (list of team strings), list_teams(), get_team(name),
# random_team(), random_teambuilder() (poke-env ConstantTeambuilder wrapper),
# and env override TEAM_NAME to pin a specific team.

import os
import random

# Collect all TEAM_* strings into a list (preserves definition order in CPython 3.7+)
TEAMS = [v for k, v in globals().items() if k.startswith("TEAM_") and isinstance(v, str)]
TEAM_POOL = TEAMS  # alias used by random_teambuilder()

def list_teams():
    """Return available team names (TEAM_A, TEAM_B, ...)."""
    return [k for k, v in globals().items() if k.startswith("TEAM_") and isinstance(v, str)]

def get_team(name: str) -> str:
    """Return the team string for a given constant name, e.g., 'TEAM_A'."""
    if name not in globals() or not isinstance(globals()[name], str):
        raise KeyError(f"Unknown team name: {name}. Available: {', '.join(list_teams())}")
    return globals()[name]

def random_team() -> str:
    """Return a random team string. Honors TEAM_NAME env var if set."""
    pinned = os.getenv("TEAM_NAME", "").strip()
    if pinned:
        return get_team(pinned)
    if not TEAMS:
        raise RuntimeError("No TEAM_* constants found in teams_ou.py")
    return random.choice(TEAMS)

def random_teambuilder():
    """Return a poke-env ConstantTeambuilder wrapping a random team string.
    Honors TEAM_NAME env var to pin a specific team (e.g., TEAM_A).
    NOTE: This picks ONE team at creation time. For per-battle randomization,
    use RandomPoolTeambuilder instead."""
    try:
        from poke_env.teambuilder.constant_teambuilder import ConstantTeambuilder
    except Exception as e:
        raise RuntimeError("poke_env not available for ConstantTeambuilder") from e
    return ConstantTeambuilder(random_team())


try:
    from poke_env.teambuilder import Teambuilder as _Teambuilder
except ImportError:
    _Teambuilder = object

class RandomPoolTeambuilder(_Teambuilder):
    """Teambuilder that picks a different random team for EACH battle.
    This eliminates team-matchup variance from evaluations."""
    def yield_team(self) -> str:
        team_str = random_team()
        # Convert showdown format to packed format (same as ConstantTeambuilder)
        if "|" in team_str:
            mons = self.parse_packed_team(team_str)
        else:
            mons = self.parse_showdown_team(team_str)
        return self.join_team(mons)


def random_pool_teambuilder():
    """Return a teambuilder that randomizes the team for every battle."""
    return RandomPoolTeambuilder()


